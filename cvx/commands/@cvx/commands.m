% CVX: Top-level commands to control CVX.
%   cvx_begin         - Starts a new CVX specification.
%   cvx_begin_set     - Starts a new cvx CVX specification.
%   cvx_cd            - Change current working directory to a CVX subdirectory. 
%   cvx_clear         - Clears all active CVX data.
%   cvx_end           - Completes a cvx specification.
%   cvx_end_set       - Completes a cvx set specification.
%   cvx_gp_precision  - Changes the precision of CVX's GP approximation.
%   cvx_pause         - Pauses the processing of CVX models.
%   cvx_power_warning - Controls the CVX warning message for x.^p expressions.
%   cvx_precision     - Controls CVX solver precision.
%   cvx_profile       - CVX-specific profiler control.
%   cvx_quiet         - CVX output control.
%   cvx_solver        - CVX solver selection.
%   cvx_where         - Returns the location of the CVX system.

